#ifndef SSColors_h
#define SSColors_h

/**
 用于重要级文字信息、内页标题信息
 如导航名称、大板块标题、类目名称等
 */
#define SSImportentColor [UIColor colorWithHexString:@"222222"]

/**
 用于普通级段落信息 引导词
 如商品详情、段落标题
 */
#define SSNormalColor [UIColor colorWithHexString:@"666666"]

/**
 用于辅助、次要的文字信息、普通按钮描边
 如好评人数、商品评论等
 */
#define SSHelpColor [UIColor colorWithHexString:@"999999"]

/**
 用于分割线、标签描边
 */
#define SSSpliterColor [UIColor colorWithHexString:@"d7d7d7"]

/**
 用于内容区域底色
 */
#define SSCoverColor [UIColor colorWithHexString:@"f3f5f7"]

/**
 用于分隔模块的底色
 */
#define SSSpliterCoverColor [UIColor colorWithHexString:@"f8f8f8"]

/**
 适用于该App的主题颜色
 */
#define SSAppThemeColor [UIColor colorWithHexString:@"546edb"]


#endif /* SSColors_h */
